 //7.반복문(Java와 동일)
 //동일한 작업을 여러번 반복할 때 사용
 
 //1~10까지 출력(document.write())
 
 //for
 for(let i = 1; i < 11 ; i++){
	document.write(i + "<br>");
}
 //while
 let nums = 1;
  while(nums < 11){
	document.write(nums + "<br>");
	nums++;
}
 
 //do~while
 
  let nums2 = 1;
  do{
	document.write(nums2 + "<br>");
	nums2++;
}while(nums < 11);




