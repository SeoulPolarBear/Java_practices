package PMain4;

public class MyName {
//이름, 나이, 성별, 키, 사는 지역, 별명
	
	public String name;
	public int age;
	public String gender;
	public double height;
	public String home;
	public String nickname;
	
	public MyName(String name,int age,String gender,double height,String home,String nickname) {
		this.name = name;
		this.age = age;
		this.gender = gender;
		this.height = height;
		this.home = home;
		this.nickname = nickname;
	}
}
