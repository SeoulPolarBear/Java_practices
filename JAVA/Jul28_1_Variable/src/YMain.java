
public class YMain {

	public static void main(String[] args) throws InterruptedException {
		//집 도착시간 1.5
		// 저녁 식사 메뉴
		//취침 시간 ?시 ?분
		//소감:
		//수업 성취도:?.?%
		//출력
		System.out.println("집 도착시간 : " + 0.5);
		System.out.print("저녁 식사 메뉴 : 햄버거\n");
		System.out.printf("%02d시 %02d분\n", 7,30);
		System.out.println("소감 : 어제 자바에서 format을 쓰는지 처음알았다. 빈 부분 또 하나 채워갔다.");
		System.out.printf("수업 성취도 : %.1f%%\n", 100.0000);
		System.out.println("출력");
		
		Thread.sleep(10000);
		

	}

}
